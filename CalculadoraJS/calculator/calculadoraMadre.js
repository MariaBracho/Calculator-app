export class Calculadora {
    constructor(op1, op2) {
        this.op1 = op1
        this.op2 = op2
    }
    Add(op1, op2) {
        return (op1 + op2)
    }
    subtraction(op1, op2) {
        return (op1 - op2)
    }
    divide(op1, op2) {
        return (op1 / op2)
    }
    multiply(op1, op2) {
        return (op1 * op2)
    }


}